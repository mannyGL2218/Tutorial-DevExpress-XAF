        private void popupPELICULAS_VisibleInvisible_Execute(object sender, SimpleActionExecuteEventArgs e)
        {
            var seleccion = e.SelectedObjects;
            foreach (BusinessObjects.Pruebas.Peliculas item in seleccion)
            {
                item.Visible = !item.Visible;
                item.Save();
                item.Session.CommitTransaction();
            }
